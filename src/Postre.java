public  abstract class Postre {

    private String nombre;
    private double tamano;
    private double precio;


    public void Preparar(){

    }
    public  void servir(){

    }

    public Postre(String nombre, double tamano, double precio) {
        this.nombre = nombre;
        this.tamano = tamano;
        this.precio = precio;
    }


}
